package com.example.calendar.model;

public enum SystemPermissionKey {

    // ==================== УПРАВЛЕНИЕ ЗАДАЧАМИ ====================
    /** Создание новой задачи */
    CREATE_TASK,

    /** Редактирование существующей задачи (название, описание, сроки) */
    EDIT_TASK,

    /** Полное удаление задачи */
    DELETE_TASK,

    /** Назначение исполнителя на задачу */
    ASSIGN_TASK,

    /** Изменение статуса задачи (To Do, In Progress, Done) */
    CHANGE_TASK_STATUS,

    /** Просмотр всех задач компании (без ограничений по отделам) */
    VIEW_ALL_TASKS,

    /** Просмотр задач только своего отдела */
    VIEW_DEPARTMENT_TASKS,

    /** Просмотр только своих задач */
    VIEW_MY_TASKS,

    /** Добавление комментария к задаче */
    ADD_COMMENT,

    /** Удаление комментария */
    DELETE_COMMENT,

    /** Редактирование комментария */
    EDIT_COMMENT,

    // ==================== УПРАВЛЕНИЕ ПРОЕКТАМИ ====================
    /** Создание нового проекта */
    CREATE_PROJECT,

    /** Редактирование параметров проекта */
    EDIT_PROJECT,

    /** Удаление проекта */
    DELETE_PROJECT,

    /** Просмотр всех проектов */
    VIEW_ALL_PROJECTS,

    /** Просмотр конкретного проекта */
    VIEW_PROJECT,

    // ==================== УПРАВЛЕНИЕ РОЛЯМИ ====================
    /** Создание новой роли (например, "Разработчик", "Тестировщик") */
    CREATE_ROLE,

    /** Редактирование параметров роли */
    EDIT_ROLE,

    /** Удаление роли */
    DELETE_ROLE,

    /** Назначение роли пользователю */
    ASSIGN_ROLE,

    /** Просмотр всех ролей */
    VIEW_ALL_ROLES,

    /** Полное управление ролями (создание, редактирование, удаление, назначение прав) */
    MANAGE_ROLES,

    // ==================== УПРАВЛЕНИЕ УРОВНЯМИ РОЛЕЙ (ROLE LEVELS) ====================
    /** Создание нового уровня роли */
    CREATE_ROLE_LEVEL,

    /** Редактирование уровня роли */
    EDIT_ROLE_LEVEL,

    /** Удаление уровня роли */
    DELETE_ROLE_LEVEL,

    /** Просмотр всех уровней ролей */
    VIEW_ALL_ROLE_LEVELS,

    /** Просмотр конкретного уровня роли */
    VIEW_ROLE_LEVEL,

    /** Назначение прав роли через уровень */
    ASSIGN_ROLE_PERMISSIONS,

    /** Просмотр прав роли */
    VIEW_ROLE_PERMISSIONS,

    // ==================== УПРАВЛЕНИЕ РАЗРЕШЕНИЯМИ ====================
    /** Создание новой схемы разрешений */
    CREATE_PERMISSION_SCHEME,

    /** Редактирование схемы разрешений */
    EDIT_PERMISSION_SCHEME,

    /** Удаление схемы разрешений */
    DELETE_PERMISSION_SCHEME,

    /** Добавление конкретного разрешения в схему */
    ADD_PERMISSION_TO_SCHEME,

    /** Удаление разрешения из схемы */
    REMOVE_PERMISSION_FROM_SCHEME,

    /** Просмотр всех схем разрешений */
    VIEW_ALL_PERMISSION_SCHEMES,

    /** Просмотр конкретной схемы разрешений */
    VIEW_PERMISSION_SCHEME,

    // ==================== КАСТОМНЫЕ ПРАВА ====================
    /** Создание нового кастомного права */
    CREATE_CUSTOM_PERMISSION,

    /** Редактирование кастомного права */
    EDIT_CUSTOM_PERMISSION,

    /** Удаление кастомного права */
    DELETE_CUSTOM_PERMISSION,

    /** Просмотр всех кастомных прав */
    VIEW_ALL_CUSTOM_PERMISSIONS,

    /** Просмотр конкретного кастомного права */
    VIEW_CUSTOM_PERMISSION,

    /** Добавление уровня к кастомному праву */
    ADD_SCHEME_LEVEL_TO_CUSTOM,

    /** Удаление уровня из кастомного права */
    REMOVE_SCHEME_LEVEL_FROM_CUSTOM,

    // ==================== УПРАВЛЕНИЕ УРОВНЯМИ (SCHEME_PERMISSIONS) ====================
    /** Создание нового уровня доступа */
    CREATE_SCHEME_PERMISSION,

    /** Редактирование уровня доступа */
    EDIT_SCHEME_PERMISSION,

    /** Удаление уровня доступа */
    DELETE_SCHEME_PERMISSION,

    /** Просмотр всех уровней доступа */
    VIEW_ALL_SCHEME_PERMISSIONS,

    /** Просмотр конкретного уровня доступа */
    VIEW_SCHEME_PERMISSION,

    // ==================== УПРАВЛЕНИЕ НАБОРАМИ (DEFINITIONS) ====================
    /** Создание нового набора прав */
    CREATE_PERMISSION_DEFINITION,

    /** Редактирование набора прав */
    EDIT_PERMISSION_DEFINITION,

    /** Удаление набора прав */
    DELETE_PERMISSION_DEFINITION,

    /** Просмотр всех наборов прав */
    VIEW_ALL_PERMISSION_DEFINITIONS,

    /** Просмотр конкретного набора прав */
    VIEW_PERMISSION_DEFINITION,

    // ==================== УПРАВЛЕНИЕ ПОЛЬЗОВАТЕЛЯМИ ====================
    /** Приглашение нового пользователя в компанию (по email) */
    INVITE_USER,

    /** Блокировка пользователя (временное отключение доступа) */
    BLOCK_USER,

    /** Полное удаление пользователя из системы */
    DELETE_USER,

    /** Редактирование данных пользователя */
    EDIT_USER,

    /** Просмотр всех пользователей */
    VIEW_ALL_USERS,

    /** Просмотр конкретного пользователя */
    VIEW_USER,

    /** Полное управление пользователями (все операции) */
    MANAGE_USERS,

    // ==================== УПРАВЛЕНИЕ ДЕПАРТАМЕНТАМИ ====================
    /** Создание нового отдела */
    CREATE_DEPARTMENT,

    /** Редактирование отдела */
    EDIT_DEPARTMENT,

    /** Удаление отдела */
    DELETE_DEPARTMENT,

    /** Просмотр всех отделов */
    VIEW_ALL_DEPARTMENTS,

    /** Просмотр конкретного отдела */
    VIEW_DEPARTMENT,

    /** Назначение пользователя в отдел */
    ASSIGN_USER_DEPARTMENT,

    /** Удаление пользователя из отдела */
    REMOVE_USER_FROM_DEPARTMENT,

    // ==================== УПРАВЛЕНИЕ КОМПАНИЕЙ ====================
    /** Редактирование информации о компании */
    EDIT_COMPANY,

    /** Просмотр информации о компании */
    VIEW_COMPANY,

    /** Управление настройками компании (SMTP, профиль компании и т.д.) */
    MANAGE_COMPANY_SETTINGS,

    // ==================== ОТЧЕТЫ И НАСТРОЙКИ ====================
    /** Просмотр аналитических отчетов */
    VIEW_REPORTS,

    /** Экспорт данных */
    EXPORT_DATA,

    /** Импорт данных */
    IMPORT_DATA,

    // ==================== АДМИНИСТРИРОВАНИЕ ====================
    /** Просмотр истории изменений */
    VIEW_AUDIT_LOG,

    /** Управление системными настройками */
    MANAGE_SYSTEM,

    /** Управление интеграциями (API, webhooks) */
    MANAGE_INTEGRATIONS,

    // ==================== УПРАВЛЕНИЕ КАТАЛОГОМ ====================
    /** Редактирование названий, описаний, иконок системных прав */
    MANAGE_PERMISSION_CATALOG,

    /** Просмотр каталога системных прав */
    VIEW_PERMISSION_CATALOG
}